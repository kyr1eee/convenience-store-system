package demo;

public class VList {
	public String hid;
	public String time;
	public String name;
	public String phone;
	public int id;
	public int getId() {
		return id;
	}
	public void setId(int i) {
		this.id = i;
	}
	public String getHid() {
		return hid;
	}
	public void setHid(String uid) {
		this.hid = uid;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
}
